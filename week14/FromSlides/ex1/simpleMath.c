#include "simpleMath.h"

int add2Ints(int a, int b)
{
	return (a + b);
}

int subtract2Ints(int a, int b)
{
	return (a - b);
}

int multiply2Ints(int a, int b)
{
	return (a * b);
}

int divide2Ints(int a, int b)
{
	return (a / b);
}

int modulus2Ints(int a, int b)
{
	return (a % b);
}
